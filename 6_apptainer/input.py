import numpy as np

# Create a 1-dimensional array
arr = np.array([1, 2, 3, 4, 5])

# Print the array
print("Array:", arr)

# Calculate the mean of an array
mn = np.mean(arr)

# Print the mean
print("Mean of array:", mn)

# Calculate the sum of an array
sm = np.sum(arr)

# Print the sum
print("Sum of array:", sm)
